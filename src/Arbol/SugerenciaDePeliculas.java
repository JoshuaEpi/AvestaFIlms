    private void cargarDatosDesdeBD() {
        // Use the CargarDatosDesdeBD class to load movie data from the database
        peliculas = CargarDatosDesdeBD.cargarPeliculasDesdeBD();

        // Populate the genero JComboBox with unique genres
        cargarGeneros();
    }
private boolean existeItemEnComboBox(JComboBox comboBox, String item) {
    for (int i = 0; i < comboBox.getItemCount(); i++) {
        if (comboBox.getItemAt(i).equals(item)) {
            return true;
        }
    }
    return false;
}
    private void cargarGeneros() {
    cbxGenero.removeAllItems();
    for (Pelicula pelicula : peliculas) {
        if (!existeItemEnComboBox(cbxGenero, pelicula.getGenero())) {
            cbxGenero.addItem(pelicula.getGenero());
        }
    }
    

    // Add ActionListener to cbxGenero to update cbxClasificacion
    cbxGenero.addActionListener(new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            // Get the selected genre
            String selectedGenero = (String) cbxGenero.getSelectedItem();

            // Update the cbxClasificacion based on the selected genre
            cargarClasificaciones(selectedGenero);

            // Clear cbxDuracion and cbxDirector
            cbxDuracion.removeAllItems();
        }
    });
}

private void cargarClasificaciones(String generoSeleccionado) {
    cbxClasificacion.removeAllItems();
    for (Pelicula pelicula : peliculas) {
        if (pelicula.getGenero().equals(generoSeleccionado)) {
            cbxClasificacion.addItem(pelicula.getClasificacionEdad());
        }
    }

    // Add ActionListener to cbxClasificacion to update cbxDuracion
    cbxClasificacion.addActionListener(new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            // Get the selected clasificacion
            String selectedClasificacion = (String) cbxClasificacion.getSelectedItem();

            // Update the cbxDuracion based on the selected clasificacion
            cargarDuraciones(generoSeleccionado, selectedClasificacion);

            // Clear cbxDirector
        }
    });
}

private void cargarDuraciones(String generoSeleccionado, String clasificacionSeleccionada) {
    cbxDuracion.removeAllItems();
    for (Pelicula pelicula : peliculas) {
        if (pelicula.getGenero().equals(generoSeleccionado) && pelicula.getClasificacionEdad().equals(clasificacionSeleccionada)) {
            cbxDuracion.addItem(String.valueOf(pelicula.getDuracion()));
        }
    }

    // Add ActionListener to cbxDuracion to update cbxDirector
    cbxDuracion.addActionListener(new ActionListener() {
        @Override
        public void actionPerformed(ActionEvent e) {
            // Get the selected duracion
            String selectedDuracion = (String) cbxDuracion.getSelectedItem();

            // Update the cbxDirector based on the selected duracion
        }
    });
}



    
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String generoSeleccionado = (String) cbxGenero.getSelectedItem();
        String clasificacionSeleccionada = (String) cbxClasificacion.getSelectedItem();
        String duracionSeleccionada = (String) cbxDuracion.getSelectedItem();

        // Fetch and display movie recommendations based on selected criteria
        mostrarRecomendaciones(generoSeleccionado, clasificacionSeleccionada, duracionSeleccionada);
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void mostrarRecomendaciones(String genero, String clasificacion, String duracion) {
        StringBuilder recomendaciones = new StringBuilder();
        for (Pelicula pelicula : peliculas) {
            if (pelicula.getGenero().equals(genero) && pelicula.getClasificacionEdad().equals(clasificacion)
                    && String.valueOf(pelicula.getDuracion()).equals(duracion)) {
                recomendaciones.append("TÃ­tulo: ").append(pelicula.getTitulo()).append(", Director: ").append(pelicula.getDirector()).append("\n");
            }
        }
        jTextArea1.setText(recomendaciones.toString());
    }
    
